package beanpack;

public class Product {
	private String name;
	private int jungga, sale;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getJungga() {
		return jungga;
	}
	
	public void setJungga(int jungga) {
		this.jungga = jungga;
	}
	
	public int getSale() {
		return sale;
	}
	
	public void setSale(int sale) {
		this.sale = sale;
	}
}
